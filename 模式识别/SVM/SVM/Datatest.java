package svm.SimplifiedSMO;

public class Datatest {
	private double x[][];
	private int y[];
	
	public Datatest(double[][] x, int[] y) {
		super();
		this.x = x;
		this.y = y;
	}

	public double[][] getX() {
		return x;
	}

	public void setX(double[][] x) {
		this.x = x;
	}

	public int[] getY() {
		return y;
	}

	public void setY(int[] y) {
		this.y = y;
	}
	
	
	
}
